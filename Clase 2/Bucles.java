public class Bucles {
    public static void main(String[] args) {
        // For(Incio; condicion de parada; incremento o decremento)
        for (int i = 1; i <= 5; i++) {
            System.out.println("Número de iteración = " + i);
        }
        // Estructura While
        int j = 1;
        while (j <= 5) {
            System.out.println("Número de iteración J: " + j);
            j++;
        }
    }
}
